-- Tabella UTENTE
CREATE TABLE UTENTE(
    ID INT AUTO_INCREMENT PRIMARY KEY,
    RUOLO ENUM('ADMIN', 'ALLEVATORE') NOT NULL,
    USERNAME VARCHAR(20) NOT NULL,
    EMAIL VARCHAR(100) NOT NULL UNIQUE,
    PASSWORD VARCHAR(255) NOT NULL,
    DATA_REGISTRAZIONE DATETIME DEFAULT CURRENT_TIMESTAMP
);

-- Tabella ANIMALE
CREATE TABLE ANIMALE(
    TAG VARCHAR(10) PRIMARY KEY,
    NOME VARCHAR(20),
    DATA_NASCITA DATE NOT NULL,
    DATA_DECESSO DATE DEFAULT NULL,
    SESSO ENUM('M', 'F') NOT NULL,
    SPECIE ENUM('Bovino', 'Suino', 'Ovino', 'Caprino', 'Equino') NOT NULL,
    RAZZA VARCHAR(20) NOT NULL,
    PADRE_TAG VARCHAR(10),
    MADRE_TAG VARCHAR(10),
    FOREIGN KEY (PADRE_TAG) REFERENCES ANIMALE(TAG) ON DELETE SET NULL,
    FOREIGN KEY (MADRE_TAG) REFERENCES ANIMALE(TAG) ON DELETE SET NULL
);

-- Gestione Produzione
CREATE TABLE TIPO_PRODOTTO(
    ID_TIPO INT AUTO_INCREMENT PRIMARY KEY,
    TIPO VARCHAR(50) NOT NULL UNIQUE
);

CREATE TABLE PRODOTTO(
    ID_PRODUZIONE INT AUTO_INCREMENT PRIMARY KEY,
    TAG_ANIMALE VARCHAR(10),
    TIPO VARCHAR(50),
    LOTTO VARCHAR(50),
    DATA DATE NOT NULL,
    QUANTITA DECIMAL(6,2) NOT NULL,
    GRASSO DECIMAL(4,2) DEFAULT NULL,
    PROTEINE DECIMAL(4,2) DEFAULT NULL,
    FOREIGN KEY (TIPO) REFERENCES TIPO_PRODOTTO(TIPO) ON DELETE CASCADE,
    FOREIGN KEY (TAG_ANIMALE) REFERENCES ANIMALE(TAG) ON DELETE CASCADE
);

-- Gestione Alimentazione
CREATE TABLE TIPO_ALIMENTO(
    ID_ALIMENTO INT AUTO_INCREMENT PRIMARY KEY,
    NOME VARCHAR(100) NOT NULL,
    TIPO VARCHAR(50)
);

CREATE TABLE ALIMENTO(
    ID_CONSUMO INT AUTO_INCREMENT PRIMARY KEY,
    TAG_ANIMALE VARCHAR(10),
    ID_ALIMENTO INT,
    DATA DATE NOT NULL,
    QUANTITA_KG DECIMAL(6,2),
    FOREIGN KEY (TAG_ANIMALE) REFERENCES ANIMALE(TAG) ON DELETE CASCADE,
    FOREIGN KEY (ID_ALIMENTO) REFERENCES TIPO_ALIMENTO(ID_ALIMENTO) ON DELETE CASCADE
);

-- Controlli Veterinari e Vaccinazioni
CREATE TABLE VETERINARIO(
    ID_VETERINARIO INT AUTO_INCREMENT PRIMARY KEY,
    NOME VARCHAR(30),
    TELEFONO VARCHAR(10)
);

CREATE TABLE CONTROLLO_VETERINARIO(
    ID_CONTROLLO INT AUTO_INCREMENT PRIMARY KEY,
    TAG_ANIMALE VARCHAR(10),
    DATA DATE NOT NULL,
    DESCRIZIONE VARCHAR(255),
    ESITO ENUM('Ottimo', 'Buono', 'Pericoloso'),
    ID_VETERINARIO INT,
    NOTE TEXT,
    FOREIGN KEY (TAG_ANIMALE) REFERENCES ANIMALE(TAG) ON DELETE CASCADE,
    FOREIGN KEY (ID_VETERINARIO) REFERENCES VETERINARIO(ID_VETERINARIO) ON DELETE SET NULL
);

CREATE TABLE VACCINAZIONE(
    ID_VACCINAZIONE INT AUTO_INCREMENT PRIMARY KEY,
    TAG_ANIMALE VARCHAR(10),
    DATA DATE NOT NULL,
    DATA_SCADENZA DATE DEFAULT NULL,
    NOME_VACCINO VARCHAR(30),
    DOSAGGIO VARCHAR(50),
    ID_VETERINARIO INT,
    NOTE TEXT,
    FOREIGN KEY (TAG_ANIMALE) REFERENCES ANIMALE(TAG) ON DELETE CASCADE,
    FOREIGN KEY (ID_VETERINARIO) REFERENCES VETERINARIO(ID_VETERINARIO) ON DELETE SET NULL
);

-- Report Certificazioni Bio
CREATE TABLE CERTIFICAZIONE(
    ID_CERTIFICAZIONE INT AUTO_INCREMENT PRIMARY KEY,
    DATA_RILASCIO DATE NOT NULL,
    DATA_SCADENZA DATE,
    ENTE_CERTIFICATORE VARCHAR(30),
    DESCRIZIONE VARCHAR(255),
    TAG_ANIMALE VARCHAR(10) NOT NULL,
    FOREIGN KEY (TAG_ANIMALE) REFERENCES ANIMALE(TAG) ON DELETE CASCADE
);

-- Spese Economiche
CREATE TABLE SPESE(
    ID_SPESA INT AUTO_INCREMENT PRIMARY KEY,
    DATA DATE NOT NULL,
    DESCRIZIONE VARCHAR(255),
    IMPORTO DECIMAL(10,2) NOT NULL,
    CATEGORIA ENUM('ALIMENTAZIONE', 'VETERINARIA', 'STRUTTURE', 'ALTRO'),
    ID_CONSUMO INT NULL
);

-- Tabelle per Statistiche e ROI
CREATE TABLE VENDITA_PRODOTTI(
    ID_VENDITA INT AUTO_INCREMENT PRIMARY KEY,
    DATA DATE NOT NULL,
    DESCRIZIONE VARCHAR(255),
    QUANTITA DECIMAL(8,2),
    IMPORTO DECIMAL(10,2) NOT NULL
);

-- Tabella ANALISI_ROI
CREATE TABLE ANALISI_ROI(
    ID INT AUTO_INCREMENT PRIMARY KEY,
    TOTALE_ENTRATE DECIMAL(12,2) DEFAULT NULL,
    TOTALE_USCITE DECIMAL(12,2) DEFAULT NULL
);

